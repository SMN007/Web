        What is cryptocurrency trading <br><br>
The main question arises in most people’s mind is what is cryptocurrency? So, cryptocurrency is the digital asset designed to work as a medium to exchange that uses cryptography to secure is transactions. It is form of decentralized digital and electronic money. The first cryptocurrency is the bitcoin and after that many cryptocurrencies came into play such as Litecoin, Ethereum, Zcash, Dash, Ripple, Monero, the Bottom line etc. Cryptocurrency is called the new gold.<br>
So, as we now know what cryptocurrency is, let’s know about Cryptocurrency trading. Cryptocurrency trading is also known as cryptocurrency exchange or Digital currency exchanges (DCE). It is the trading in which businesses that allows customers to trade cryptocurrencies or digital currencies for other assets, such as conventional flat money or different digital currency. There are also altcoins (altcoins are the cryptos that are not bitcoins). In cryptocurrency trading, altcoins can be paired with bitcoins. <br>
Cryptocurrency trading is just and alternative way to get involved in the crypto-world. Cryptocurrency trading doesn’t require mining hardware nor investing in bitcoin hyips or bitcoin cloud mining. 
To start cryptocurrency trading, you first of all need a wallet, without wallet you cannot buy any cryptocurrency. The majority of the Cryptocurrency trading or exchanges have a free wallet along with the ability to trade. But in cryptocurrency trading, the users are suggested not to keep all their cryptos at one place because they can get hacked or scammed. <br><br>
You must be familiar with an idiom saying: “Trade the rumor- sell the news.
This is an old saying in the share exchange world but it is also valid in cryptocurrency trading.
There are some golden rules which has to be followed in the cryptocurrency trading world.<br><br>
Always do your homework and read the fundamentals of a coin. If you read the whitepapers, you will know more than 90% of the people we know trade cryptos.<br>
Go to crypto events. That is the place you will find the hottest coins!<br>
Buy weeks before a roadmap (of development) gets revealed. It is like the stock earning releases – something gets so hyped-up that even a good report can’t help it.<br>
Never buy into a coin after a dump.<br>
Learn to understand the Block (or any) -chain.<br>
Learn to understand the correlation between Bitcoin and an altcoin.<br>
Learn to understand candlestick charts. They offer significant information, which can’t be figured out of normal lined charts.<br>
Secure winning & limit losses<br>
Learn technical analysis. This plays a major role in reading current market movement.<br>
Don’t trade if you lose your sleep because of a single trade. That is just not worth it.<br>
 
